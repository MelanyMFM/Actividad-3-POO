    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        String codigo, nombre;
        double horasTrabajadas, valorHora, retencion, salarioNeto, salarioBruto;
        
        codigo = txtCodigo.getText();
        nombre = txtNombre.getText();
        horasTrabajadas = Double.parseDouble(txtHorastrabajadas.getText());
        valorHora = Double.parseDouble(txtValor.getText());
        retencion = Double.parseDouble(txtRetencion.getText());
        
        salarioBruto = horasTrabajadas * valorHora;
        double retencionn = salarioBruto * (retencion / 100);
        salarioNeto = salarioBruto - retencionn;
        
        txtSalariobruto.setText(String.valueOf(salarioBruto));
        txtSalarioneto.setText(String.valueOf(salarioNeto));
    }//GEN-LAST:event_btnCalcularActionPerformed

